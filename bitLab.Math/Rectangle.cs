using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace bitLab.Math
{
  public class Rectangle
  {
    public readonly double X, Y, Width, Height;

    public Rectangle(double x, double y, double w, double h)
    {
      X = x;
      Y = y;
      Width = w;
      Height = h;
    }
  }
}
